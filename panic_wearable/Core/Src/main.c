static void check_button() { // check button state
	bool new_button_on = HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_13);
	if (prev_button_on == false && new_button_on == true) {
		if (panic_mode) {
			unpanic();
		} else {
			panic();
		}
		panic_mode = !panic_mode;
	}
	prev_button_on = new_button_on;
}

static void panic() {
	printf("Panic!\n");
	fflush(stdout);

	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_6, GPIO_PIN_SET); // light up blue LED
	music_start();
//	HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1); // start IR transmitter
	HAL_TIM_Base_Start_IT(&htim4); // start motivational messages
}

static void unpanic() {
	printf("Unpanic!\n");
	fflush(stdout);
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_6, GPIO_PIN_RESET); // turn off blue LED
	music_stop();
//	HAL_TIM_PWM_Stop(&htim1, TIM_CHANNEL_1); // stop IR transmitter
	HAL_TIM_Base_Stop_IT(&htim4); // stop motivational messages
	Lcd_clear(&lcd);
	handle_will_panic(); // turn on panic attack predictor
}

static void handle_will_panic() { // display panic attack prediction
	if (panic_mode || !will_panic() || panic_min == will_panic_in_min()) {
		return;
	}
	if (user_is_panicking) { // if timer reaches 0
		panic();
		user_is_panicking = false;
		return;
	}
	Lcd_clear(&lcd);
	Lcd_cursor(&lcd, 0, 0);
	char message[CHAR_PER_LINE + 1];
	sprintf(message, "PANIC in %d min", will_panic_in_min());
	Lcd_string(&lcd, message);
	Lcd_cursor(&lcd, 1, 0);
	Lcd_string(&lcd, "66.6% chance");
	panic_min = will_panic_in_min();
}

void handle_msg_it() { // handle motivational message interrupt
	Lcd_clear(&lcd);
	if (msg_finished) { // reset if current message finished
		msg_idx = rand() % NUM_MSG;
		msg_finished = false;
		msg_line = 0;
	}
	// hacky null char swapping to print each line
	for (int i = 0; i < NUM_LINES; i++) {
		Lcd_cursor(&lcd, i, 0);
		int end_ch_idx = (msg_line + i + 1) * CHAR_PER_LINE;
		if (end_ch_idx < msg_strlen[msg_idx]) {
			char *to_null = &msg[msg_idx][end_ch_idx];
			char ch = *to_null;
			*to_null = '\0';
			Lcd_string(&lcd, &msg[msg_idx][(msg_line + i) * CHAR_PER_LINE]);
			*to_null = ch;
		} else {
			Lcd_string(&lcd, &msg[msg_idx][(msg_line + i) * CHAR_PER_LINE]);
			msg_finished = true;
		}
	}
	msg_line++;
}
